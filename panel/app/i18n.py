def get_locale(request: Request) -> str:
    lang = request.cookies.get(COOKIE_NAME)
    if lang in TRANSLATIONS:
        return lang
    # Try Accept-Language as a best-effort first-visit hint.
    accept = request.headers.get("accept-language", "").lower()
    if accept.startswith("ru"):
        return "ru"
    return DEFAULT_LOCALE


def translator(locale: str):
    table = TRANSLATIONS.get(locale, TRANSLATIONS[DEFAULT_LOCALE])
    fallback = TRANSLATIONS[DEFAULT_LOCALE]

    def t(key: str, /, **kwargs) -> str:
        text = table.get(key) or fallback.get(key) or key
        if kwargs:
            try:
                return text.format(**kwargs)
            except (KeyError, IndexError):
                return text
        return text

    return t
